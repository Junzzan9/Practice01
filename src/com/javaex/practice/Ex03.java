package com.javaex.practice;

public class Ex03 {

	public static void main(String[] args) {

		//예상답안 1) a=4 2) b=2 3) x=2 4) y=2
		
		int x = 1;
		int y = 1;
		
		int a = ++x*2;
		int b = y++*2;
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("x="+x);
		System.out.println("y="+y);
		
		//예상과 일치했다.

	}

}
